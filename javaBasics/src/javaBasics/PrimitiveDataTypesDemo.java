package javaBasics;

public class PrimitiveDataTypesDemo {
	public static void main(String[] args) {
		
		int age = 25;
		float cgpa = 5.9f;
		double weight = 72.456;
		char teamId = 'A';
		boolean isPresent = true;

		System.out.println("Integer (int) value: " + age);
		System.out.println("Float (float) value: " + cgpa);
		System.out.println("Double (double) value: " + weight);
		System.out.println("Character (char) value: " + teamId);
		System.out.println("Boolean (boolean) value: " + isPresent);
	}
}
